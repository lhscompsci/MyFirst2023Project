public class FirstJavaProgram {     // This is a comment to describe what this Java class does

    // This is the main method demonstrating how to output to the console.
    public static void main(String[] args) {
        System.out.println("Howdy y'all!!");
        System.out.print("How's it going?");
        System.out.println("I'm good.");

        //printf -- formatted print

        //-------------
        // Escape sequences -
        System.out.println("Hello\n World");  // newline character
        System.out.println("Howdy\t y'all!\b");  // tab character and backspace character


    }
}